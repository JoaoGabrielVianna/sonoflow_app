/// Enumeração que define os tipos de entrada (input) possíveis
/// no formulário ou componentes da interface do usuário.
/// - `date`: Representa uma entrada do tipo data.
/// - `time`: Representa uma entrada do tipo hora.
/// - `text`: Representa uma entrada do tipo texto.
enum InputType {
  date,
  time,
  text,
  select,
  count,
  slider,
  choiceChip,
  bool
}


